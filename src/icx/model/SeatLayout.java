    public void loadflights() {

        try {

            ResultSet resultSet = MySQL.execute("SELECT * FROM `Flights` ");

            Vector<String> vector = new Vector<>();
            vector.add("Select Flight");

            while (resultSet.next()) {

                vector.add(resultSet.getString("flight_number"));
                FlightMap.put(resultSet.getString("flight_number"), resultSet.getString("flight_id"));

            }

            selecFlight.setModel(new DefaultComboBoxModel<>(vector));

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

    public void loadClass() {

        try {

            ResultSet resultSet = MySQL.execute("SELECT * FROM `class` ");

            Vector<String> vector = new Vector<>();
            vector.add("Class Selection");

            while (resultSet.next()) {

                vector.add(resultSet.getString("class_type"));
                seatClassMap.put(resultSet.getString("class_type"), resultSet.getString("c_id"));

            }

            classSelect.setModel(new DefaultComboBoxModel<>(vector));

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

    private void loadCustomers(String column, String type, String mobile) {

        try {

            ResultSet resultSet = MySQL.execute("SELECT * FROM `seat` INNER JOIN `Flights` ON `seat`.`Flights_flight_id`=`Flights`.`flight_id` "
                    + "WHERE `Flight_flight_id` =  '" + FlightMap.get(String.valueOf(selecFlight.getSelectedIndex())) + "' ");

            DefaultTableModel model = (DefaultTableModel) seatLayoutTable.getModel();
            model.setRowCount(0);

            while (resultSet.next()) {

                Vector<String> vector = new Vector<>();
                vector.add(resultSet.getString("mobile"));
                vector.add(resultSet.getString("first_name"));
                vector.add(resultSet.getString("last_name"));
                vector.add(resultSet.getString("email"));
                vector.add(resultSet.getString("points"));

                model.addRow(vector);
            }

        } catch (Exception e) {
            e.printStackTrace();

        }

    }

