    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String flight = String.valueOf(selecFlight.getSelectedItem());
        String sclass = String.valueOf(classSelect.getSelectedItem());
        String id = String.valueOf(seatId.getText());

        if (flight.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Select Flight", "Warning", JOptionPane.ERROR_MESSAGE);

        } else if (id.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Enter seat Location Id", "Warning", JOptionPane.ERROR_MESSAGE);

        } else if (sclass.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Select class", "Warning", JOptionPane.ERROR_MESSAGE);

        } else {

            try {

                MySQL.execute("INSERT INTO `seat` (`s_id`,`c_id`,`b_id`,`Flights_flight_id`)"
                        + "VALUES('" + id + "','" + seatClassMap.get(sclass) + "','1','" + FlightMap.get(flight) + "')");

                DefaultTableModel model = (DefaultTableModel) seatLayoutTable.getModel();

                Vector<String> vector = new Vector<>();
                vector.add(id);
                vector.add(sclass);
                vector.add("Active");

                model.addRow(vector);

            } catch (Exception e) {
                e.printStackTrace();
            }

        }

    }//GEN-LAST:event_jButton1ActionPerformed



