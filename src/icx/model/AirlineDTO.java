package icx.model;

/**
 *
 * @author sandaruwan
 */
public class AirlineDTO {

    private int airlineID;
    private String airlineName;
    private String airlineCode;
    private String airlineCountry;
    private String airlineStatus;

    /**
     * @return the airlineID
     */
    public int getAirlineID() {
        return airlineID;
    }

    /**
     * @param airlineID the airlineID to set
     */
    public void setAirlineID(int airlineID) {
        this.airlineID = airlineID;
    }

    /**
     * @return the airlineName
     */
    public String getAirlineName() {
        return airlineName;
    }

    /**
     * @param airlineName the airlineName to set
     */
    public void setAirlineName(String airlineName) {
        this.airlineName = airlineName;
    }

    /**
     * @return the airlineCode
     */
    public String getAirlineCode() {
        return airlineCode;
    }

    /**
     * @param airlineCode the airlineCode to set
     */
    public void setAirlineCode(String airlineCode) {
        this.airlineCode = airlineCode;
    }

    /**
     * @return the airlineCountry
     */
    public String getAirlineCountry() {
        return airlineCountry;
    }

    /**
     * @param airlineCountry the airlineCountry to set
     */
    public void setAirlineCountry(String airlineCountry) {
        this.airlineCountry = airlineCountry;
    }

    /**
     * @return the airlineStatus
     */
    public String getAirlineStatus() {
        return airlineStatus;
    }

    /**
     * @param airlineStatus the airlineStatus to set
     */
    public void setAirlineStatus(String airlineStatus) {
        this.airlineStatus = airlineStatus;
    }

}
