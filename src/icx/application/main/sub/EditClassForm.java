    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select a class to update", "warning", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Assuming the first column contains the ID
        String class_id = jTable1.getValueAt(selectedRow, 0).toString();
        String class_name = jTextField1.getText();
        String amenities = jTextArea1.getText();

        if (class_name.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Class Name", "warning", JOptionPane.WARNING_MESSAGE);
        } else if (amenities.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Amenities", "warning", JOptionPane.WARNING_MESSAGE);
        } else {
            try {
        
                MySQL.execute("UPDATE class SET class_type = '" + class_name + "', dis = '" + amenities + "' WHERE c_id = " + class_id);
                JOptionPane.showMessageDialog(this, "<html><span style='color: green;'>✔ Class updated successfully</span></html>", "info", JOptionPane.INFORMATION_MESSAGE);

                jTextField1.setText("");
                jTextArea1.setText("");

                refreshTable();

            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Error updating class: " + e.getMessage(), "error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

