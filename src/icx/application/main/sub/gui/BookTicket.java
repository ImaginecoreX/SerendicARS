    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // Create Ticket
        String passport = passport_id.getText();
        String price = priceField.getText();
        String issueDate;
        String expireDate;
        String ticketType = String.valueOf(ticketTypeBox1.getSelectedItem());
        String scheduleId = getScheduleID().getText();
        String seatId = String.valueOf(seatBox.getSelectedItem());

        if (passport.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Select Passeneger", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (price.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Price", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (jDateChooser1.getDate() == null) {
            JOptionPane.showMessageDialog(this, "Please Select Issue Date", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (jDateChooser2.getDate() == null) {
            JOptionPane.showMessageDialog(this, "Please Select Expire Date", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (ticketType.toLowerCase().equals("select ticket type")) {
            JOptionPane.showMessageDialog(this, "Please Select Ticket Type", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (scheduleId.toLowerCase().equals("schedule id")) {
            JOptionPane.showMessageDialog(this, "Please Select Flight Schedule", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (seatId.toLowerCase().equals("select seat")) {
            JOptionPane.showMessageDialog(this, "Please Select Seat No", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            issueDate = new DateFormatter().formatDate(jDateChooser1.getDate());
            expireDate = new DateFormatter().formatDate(jDateChooser2.getDate());
            try {
                MySQL.execute("INSERT INTO `ticket` (`p_id`,`lssued_date`,`expire_date`,`tiket_type_id`, `flight_schedule_id`, `price`, `seat_s_id`) "
                        + "VALUES ('" + passenger.getId() + "', '" + issueDate + "', '" + expireDate + "', '" + ticketMap.get(ticketType) + "', '" + scheduleId + "', '" + price + "', '" + seattMap.get(seatId) + "')");
                JOptionPane.showMessageDialog(this, "Ticket Issued Successfully", "Success", JOptionPane.INFORMATION_MESSAGE);

                // Print Report
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Ticket Insert Failed", "Warning", JOptionPane.WARNING_MESSAGE);
                System.out.println(e.getMessage());
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

