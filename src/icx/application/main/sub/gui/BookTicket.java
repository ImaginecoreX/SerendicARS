    private void loadTicketType() {
        Vector<String> vector = new Vector<>();
        vector.add("Select Ticket Type");
        try {
            ResultSet resultset = MySQL.execute("SELECT * FROM `ticket_type`");
            while (resultset.next()) {
                vector.add(resultset.getString("type"));
                ticketMap.put(resultset.getString("type"), resultset.getString("id"));
                DefaultComboBoxModel model = new DefaultComboBoxModel(vector);
                ticketTypeBox1.setModel(model);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    public void loadSeat() {
        String query = "SELECT * FROM `seat` "
                + "INNER JOIN `class` ON `seat`.`c_id`=`class`.`c_id`"
                + "INNER JOIN `flights` ON `seat`.`flights_flight_id` = `flights`.`flight_id`";

        if (!scheduleID.getText().equals("Schedule ID")) {
            try {
                ResultSet flightRs = MySQL.execute("SELECT `flight_id` FROM `flight_schedule` WHERE `id`='" + scheduleID.getText() + "'");
                if (flightRs.next()) {
                    query += " WHERE `flights_flight_id`='" + flightRs.getString("flight_id") + "'";
                }

            } catch (SQLException ex) {
                Logger.getLogger(BookTicket.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

        Vector<String> vector = new Vector<>();
        vector.add("Select Seat");

        try {
            ResultSet resultset = MySQL.execute(query);
            while (resultset.next()) {
                seattMap.put(resultset.getString("s_id") + " : " + resultset.getString("class_type") + " : " + resultset.getString("flight_number"), resultset.getString("s_id"));

                vector.add(resultset.getString("s_id") + " : " + resultset.getString("class_type") + " " + resultset.getString("flight_number"));
                DefaultComboBoxModel model = new DefaultComboBoxModel(vector);

                seatBox.setModel(model);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

